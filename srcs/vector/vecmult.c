#include "vector.h"

t_vec3lf
    vecmult(const t_vec3lf a, const t_vec3lf b)
{
    t_vec3lf    mult;
    
    mult.x = a.x * b.x;
    mult.y = a.y * b.y;
    mult.z = a.z * b.z;
    return (mult);
}
